# Security band from system security_status (nullsec only; high/low sec not in bands).
# sec > -0.25 -> 0; -0.46 < sec <= -0.25 -> 1; -0.66 < sec <= -0.45 -> 2;
# -0.86 < sec <= -0.65 -> 3; sec <= -0.85 -> 4
SEC_BAND_BOUNDS = (-0.25, -0.46, -0.65, -0.86)


def security_band_from_status(security_status: float) -> int | None:
    """Return 0–4 for nullsec bands, or None if system is not nullsec (sec > -0.25 is band 0 but highsec/lowsec)."""
    if security_status > -0.25:
        return 0
    if security_status > -0.46:
        return 1
    if security_status > -0.66:
        return 2
    if security_status > -0.86:
        return 3
    return 4


